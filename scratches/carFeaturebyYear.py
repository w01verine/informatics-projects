car_year = int(input('Enter car year: '))

if car_year < 1968:
    print('Probably has few safety features.')

if car_year > 1970:
    print('Probably has seat belts.')

if car_year > 1992:
    print('Probably has anti-lock brakes.')

if car_year > 2001:
    print('Probably has airbags.')

if car_year > 2009:
    print('Probably has Bluetooth.')

if car_year > 2015:
    print('Probably has a cruise control.')

if car_year > 2020:
    print('Probably has a parking assist system.')
